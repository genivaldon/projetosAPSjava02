package br.ufpb.dcx.aps.atividades.atv02;

public class Professor {

    private String nome;

    public Professor() {
        this.nome = "";
    }

    public Professor(String nome) {
        this.nome = nome;
    }

    public String getNome() {
        return this.nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

}
